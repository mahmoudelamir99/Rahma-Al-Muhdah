import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const require = createRequire(import.meta.url);

const { Scanner } = require("./al-rahma-recruitment-admin/node_modules/@tailwindcss/oxide");
const { compile } = await import(
  pathToFileUrl(
    path.join(
      __dirname,
      "al-rahma-recruitment-admin",
      "node_modules",
      "@tailwindcss",
      "node",
      "dist",
      "index.mjs"
    )
  )
);

const inputPath = path.join(__dirname, "tailwind-site.input.css");
const outputPath = path.join(__dirname, "assets", "tailwind-site.css");
const inputCss = await readFile(inputPath, "utf8");

const scanner = new Scanner({
  sources: [
    { base: __dirname, pattern: "*.html", negated: false },
    { base: __dirname, pattern: "site.js", negated: false },
  ],
});

const compiler = await compile(inputCss, {
  base: __dirname,
  from: inputPath,
  onDependency() {},
});

const candidates = scanner.scan();
const css = compiler.build(candidates);

await writeFile(
  outputPath,
  `/* Generated by build-tailwind-site.mjs. Do not edit directly. */\n${css}\n`,
  "utf8"
);

console.log(`Built ${path.relative(__dirname, outputPath)} with ${candidates.length} candidates.`);

function pathToFileUrl(targetPath) {
  const normalizedPath = path.resolve(targetPath).replace(/\\/g, "/");
  return `file:///${normalizedPath}`;
}
